class Solution
{
    public:
    // Function to check if Kth bit is set or not.
  //  this is done when k is given  in   accordance of zero based indexing
  // suppose (000100) then k =2 givea true  
    bool checkKthBit(int n, int k)
    {
       if(((n>>k)&1)==1) return true;
       else 
          return false;
    }
};

//{ Driver Code Starts.

// Driver Code
int main()
{
	int t;
	cin>>t;//taking testcases
	while(t--)
	{
	    long long n;
	    cin>>n;//input n
	    int k;
	    cin>>k;//bit number k
	    Solution obj;
	    if(obj.checkKthBit(n, k))
	        cout << "Yes" << endl;
	    else
	        cout << "No" << endl;
	}
	return 0;
}
